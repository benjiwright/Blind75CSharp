   [Fact]
   public void ShortestBridge_DoubleBridge()
   {
      var grid = new[]
      {
         new[] {0, 1, 1, 0},
         new[] {0, 0, 0, 0},
         new[] {0, 0, 0, 0},
         new[] {0, 0, 1, 1}
      };

      var testObj = new ShortestBridgeSolver();
      testObj.ShortestBridge(grid).Should().Be(2);
   }

   [Fact]
   public void ShortestBridge_CircleIsland()
   {
      var grid = new[]
      {
         new[] {1, 1, 1, 1, 1},
         new[] {1, 0, 0, 0, 1},
         new[] {1, 0, 1, 0, 1},
         new[] {1, 0, 0, 0, 1},
         new[] {1, 1, 1, 1, 1}
      };

      var testObj = new ShortestBridgeSolver();
